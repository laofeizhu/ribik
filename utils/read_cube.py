import numpy as np

def read_cube_arr(color):
  color = ''.join(color.split())
  color_dict = {
      color[4]: 0,
      color[13]: 1,
      color[22]: 2,
      color[31]: 3,
      color[40]: 4,
      color[49]: 5
  }
  arr = np.array([[[ 1,  1,  1,  1,  1],
        [ 1, 51, 52, 53,  1],
        [ 1, 48, 49, 50,  1],
        [ 1, 45, 46, 47,  1],
        [ 1,  1,  1,  1,  1]],

       [[ 1,  9, 10, 11,  1],
        [18,  1,  1,  1, 29],
        [21,  1,  1,  1, 32],
        [24,  1,  1,  1, 35],
        [ 1, 42, 43, 44,  1]],

       [[ 1, 12, 13, 14,  1],
        [19,  1,  1,  1, 28],
        [22,  1,  1,  1, 31],
        [25,  1,  1,  1, 34],
        [ 1, 39, 40, 41,  1]],

       [[ 1, 15, 16, 17,  1],
        [20,  1,  1,  1, 27],
        [23,  1,  1,  1, 30],
        [26,  1,  1,  1, 33],
        [ 1, 36, 37, 38,  1]],

       [[ 1,  1,  1,  1,  1],
        [ 1,  0,  1,  2,  1],
        [ 1,  3,  4,  5,  1],
        [ 1,  6,  7,  8,  1],
        [ 1,  1,  1,  1,  1]]], dtype=np.uint8)
  for x in range(5):
    for y in range(5):
      for z in range(5):
        arr[(x, y, z)] = color_dict[color[arr[(x, y, z)]]]
  return arr
